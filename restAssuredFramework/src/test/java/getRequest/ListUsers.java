package getRequest;

public class ListUsers {
	int id;
	String email;
	String first_name;
	String last_name;
	String avatar;
	public ListUsers(int id, String email, String first_name, String last_name, String avatar) {
		super();
		this.id = id;
		this.email = email;
		this.first_name = first_name;
		this.last_name = last_name;
		this.avatar = avatar;
	}
	
}